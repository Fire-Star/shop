package exam.service;

public class ShoppingBreakException extends Exception{

	public ShoppingBreakException() {
		super();
	}

	public ShoppingBreakException(String message, Throwable cause) {
		super(message, cause);
	}

	public ShoppingBreakException(String message) {
		super(message);
	}

	public ShoppingBreakException(Throwable cause) {
		super(cause);
	}
	
}
