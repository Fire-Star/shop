package exam.service;

public class VertifyCodeException extends Exception {

	public VertifyCodeException() {
		super();
	}

	public VertifyCodeException(String message, Throwable cause) {
		super(message, cause);
	}

	public VertifyCodeException(String message) {
		super(message);
	}

	public VertifyCodeException(Throwable cause) {
		super(cause);
	}
	
}
