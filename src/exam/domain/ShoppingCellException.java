package exam.domain;

public class ShoppingCellException extends Exception{

	public ShoppingCellException() {
		super();
	}

	public ShoppingCellException(String message, Throwable cause) {
		super(message, cause);
	}

	public ShoppingCellException(String message) {
		super(message);
	}

	public ShoppingCellException(Throwable cause) {
		super(cause);
	}
	
}
